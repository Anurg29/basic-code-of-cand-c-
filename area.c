/*  AREA OF RECTANGLE */

#include<stdio.h>
int main(){
    float l,b;
    printf("enter the value of l=");
    scanf("%f",&l);
    printf("enter the value of b=");
    scanf("%f",&b);
    float area=l*b;
    printf("area of rectangle = %f",area);
    float perimeter=2*(l+b);
printf("\narea of perimeter = %f",perimeter);
return 0;
}